// k6 load test — ticket creation throughput target ~1000 RPS.
// Run: k6 run -e API_URL=http://localhost:4000 infra/k6/ticket-creation.js
import http from 'k6/http';
import { check, sleep } from 'k6';

const API_URL = __ENV.API_URL || 'http://localhost:4000';

export const options = {
  scenarios: {
    ticket_creation: {
      executor: 'ramping-arrival-rate',
      startRate: 50,
      timeUnit: '1s',
      preAllocatedVUs: 200,
      maxVUs: 1000,
      stages: [
        { target: 200, duration: '30s' },
        { target: 1000, duration: '1m' },
        { target: 1000, duration: '2m' },
        { target: 0, duration: '30s' },
      ],
    },
  },
  thresholds: {
    http_req_failed: ['rate<0.01'],
    http_req_duration: ['p(95)<500'],
  },
};

export default function () {
  const payload = JSON.stringify({
    subject: `Load test ticket ${__VU}-${__ITER}`,
    requesterEmail: `load-${__VU}@example.com`,
    requesterName: `Load User ${__VU}`,
    contents: 'Generated by k6 load test.',
  });
  const res = http.post(`${API_URL}/api/tickets/public`, payload, {
    headers: { 'Content-Type': 'application/json' },
  });
  check(res, { 'status is 201/200': (r) => r.status === 201 || r.status === 200 });
  sleep(0.1);
}
